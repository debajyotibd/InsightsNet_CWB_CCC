/*

==========
COMMENTARY
==========

First, some musings about the creation procedure for these tables
(which I did on a throwaway VM  in order to not muckup my own settings).

PROCEDURE:

download pcre code (8.45)

compile:
	./configure --enable-rebuild-chartables && make

now we can use dftables executable to create the appropriate char tables.

eg
	LC_ALL=ar_EG.iso88596 ./dftables -L tbl-8859-6.c

but, we gotta enable the locale first:

Find a suitable locale  in /usr/share/i18n/SUPPORTED   (table of the ones I found is below) and then run
    sudo locale-gen ar_EG
    locale -a   # check they all got generated


Charset              Locale to use
====================================
ascii  = ?????
latin1 = 8859-1       en_GB
latin2 = 8859-2       cs_CZ
latin3 = 8859-3       mt_MT
latin4 = 8859-4                       not found - on Ubuntu, the langs supposed to use latin4 use others instead.
                                      (NB: latin4, latin6, latin7 are 3 incompatible ways to handle chars for N Europe.
									                    latin4 is closest to latin 6. latin4 tables could be created by looking at what's
									                    different between 6 and 4, and settign the bytes appropriately. In theory!)
cyrillic = 8859-5     ru_RU
arabic = 8859-6       ar_EG
greek  = 8859-7       el_GR
hebrew = 8859-8       he_IL
latin5 = 8859-9       tr_TR
latin6 = 8859-10      lg_UG           supposed to be for northern European langs but this is the only locale on Ubuntu that is latin6
latin7 = 8859-13      lt_LT
latin8 = 8859-14      cy_GB
latin9 = 8859-15      en_GB.ISO-8859-15

for lc in en_GB.ISO-8859-15 en_GB cs_CZ mt_MT ru_RU el_GR he_IL tr_TR lg_UG lt_LT cy_GB ; do sudo locale-gen $lc ; done

LC_ALL=en_GB.iso88591  ./dftables -L tbl-8859-1.c
LC_ALL=cs_CZ.iso88592  ./dftables -L tbl-8859-2.c
LC_ALL=mt_MT.iso88593  ./dftables -L tbl-8859-3.c
LC_ALL=ru_RU.iso88595  ./dftables -L tbl-8859-5.c
LC_ALL=ar_EG.iso88596  ./dftables -L tbl-8859-6.c
LC_ALL=el_GR.iso88597  ./dftables -L tbl-8859-7.c
LC_ALL=he_IL.iso88598  ./dftables -L tbl-8859-8.c
LC_ALL=tr_TR.iso88599  ./dftables -L tbl-8859-9.c
LC_ALL=lg_UG.iso885910 ./dftables -L tbl-8859-10.c
LC_ALL=lt_LT.iso885913 ./dftables -L tbl-8859-13.c
LC_ALL=cy_GB.iso885914 ./dftables -L tbl-8859-14.c
LC_ALL=en_GB.iso885915 ./dftables -L tbl-8859-15.c

With those notes recorded, we can move on to the actual tables - collected together into an array.

From each file above, the table creation code as a global array was copy-pasted into here.

(and not altered except for its indent).

========================================================================================================================
*/


